#include <iostream>

/*
Os operadores aritmeticos são simbolos utilizados para realizar operações matematicas como:
*/

int main(){
	
	//Soma -> ( + )
	int numero_1 = 10 + 10; // Resultado : variavel = 20
	
	//Subtração-> ( - )
	int numero_2 = 270 - 120; // Resultado : variavel = 150
		
	//Multiplicação -> ( * )
	int numero_3 = 34 * 2; // Resultado : variavel = 68

	//Divisão -> ( / )
	int numero_4 = 180 / 4; // Resultado : variavel = 45

	//Módulo -> ( % )
	int numero_5 = 100 % 3; // Resultado : variavel = 1
	
	return 0;
}

